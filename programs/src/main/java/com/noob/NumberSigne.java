package com.noob;

public class NumberSigne {
  public static void main(String[] args) {
      int number = 1;
      if (number < 0){
        System.out.println("Le nombre entré est Négatif.");
      }else if(number == 0){
        System.out.println("Le nombre entré est Zéro.");
      }else{
        System.out.println("Le nombre entré est Positif.");
      }
  }
}
